#include <iostream>

using namespace std;

//Se declaran 2 variables
int main() {
    int a=23,b=12;

//Utilizar Operadores + - / * %
    cout << "La suma de " << a << " + " << b << " es " << a+b << endl;
    cout << "La resta de " << a << " - " << b << " es " << a-b << endl;
    cout << "La división de " << a << " / " << b << " es " << a/b << endl;
    cout << "La multiplicación de " << a << " * " << b << " es " << a*b << endl;
    cout << "El módulo de " << a << " % " << b << " es " << a%b << endl;

    return 0;
}
